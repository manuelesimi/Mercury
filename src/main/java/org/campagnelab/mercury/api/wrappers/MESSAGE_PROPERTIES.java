package org.campagnelab.mercury.api.wrappers;

/**
 * Pre-defined properties that can be associated to messages.
 *
 * @author manuele
 */
public enum MESSAGE_PROPERTIES {
    /**
     * Protocol buffer class name that is serialized in the message
     */
    PB_CLASS_NAME,
    /**
     * The type of the message.
     */
    MESSAGE_TYPE
}
